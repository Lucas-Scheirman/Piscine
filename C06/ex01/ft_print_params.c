#include <unistd.h>

int	main(int argc, char **argv)

{
	int	i;
	int	o;

	i = 1;
	o = 0;
	if (argc > 1)
	{
		while (argv[i])
		{
			while (argv[i][o])
			{
				write(1, &argv[i][o], 1);
				o++;
			}
			write(1, "\n", 1);
			o = 0;
			i++;
		}
	}
	return (0);
}
