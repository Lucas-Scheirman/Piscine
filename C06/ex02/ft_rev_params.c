#include <unistd.h>

int	main(int argc, char **argv)
{
	int	i;
	int	o;

	if (argc > 1)
	{
		i = argc - 1;
		o = 0;
		while (i > 0)
		{
			o = 0;
			while (argv[i][o])
			{
				write(1, &argv[i][o], 1);
				o++;
			}
			write(1, "\n", 1);
			i--;
		}
	}
	return (0);
}
